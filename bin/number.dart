void main() {
  // Number
  // Number adalah tipe data angka, terdapat dua jenis tipe data Number, int dan double

  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  print("===");

  // num
  // Jika kita ingin menggunakan tipe data number yang bisa int ataupun double, kita bisa menggunakan tipe data num

  num number3 = 4;
  print(number3);
}
