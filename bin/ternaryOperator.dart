void main() {
  /*
  * Ternary operator adalah operator sederhana dari if
    Ternary operator terdiri dari  kondisi yang dievaluasi,
    jika menghasilkan true maka nilai pertama diambil, jika false, maka nilai kedua diambil
  * */

  var nilai = 75;
  var ucapan = nilai >= 75 ? "Selamat anda lulus" : "Silahkan coba lagi";

  print(ucapan);
}